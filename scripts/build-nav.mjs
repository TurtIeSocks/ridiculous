import { mkdirSync, readFileSync, writeFileSync } from "node:fs"
import { dirname, resolve } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const registryPath = resolve(__dirname, "../registry.json")
const registry = JSON.parse(readFileSync(registryPath, "utf8"))

const items = registry.items.map((it) => ({
  name: it.name,
  title: it.title,
  description: it.description,
  href: `/${it.name}/`,
}))

const out = `// AUTO-GENERATED by scripts/build-nav.mjs. Do not edit.
export interface NavItem {
  name: string
  title: string
  description: string
  href: \`/\${string}/\`
}

export const NAV = ${JSON.stringify(items, null, 2)} as const satisfies readonly NavItem[]
`

const outDir = resolve(__dirname, "../src/generated")
mkdirSync(outDir, { recursive: true })
writeFileSync(resolve(outDir, "nav.ts"), out)

console.log(`Wrote ${items.length} nav items to src/generated/nav.ts`)
